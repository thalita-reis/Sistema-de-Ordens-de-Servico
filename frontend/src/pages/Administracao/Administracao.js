import React, { useState, useEffect } from 'react';
import {
  Box,
  Button,
  Grid,
  Paper,
  TextField,
  Typography,
  CircularProgress,
} from '@mui/material';
import { Save } from '@mui/icons-material';
import { toast } from 'react-toastify';
import { useForm, Controller } from 'react-hook-form';

function Administracao() {
  const [loading, setLoading] = useState(true);
  const [saving, setSaving] = useState(false);

  const {
    control,
    handleSubmit,
    setValue,
    reset,
    watch
  } = useForm({
    defaultValues: {
      razao_social: '',
      nome_oficina: '',
      cnpj: '',
      inscricao_estadual: '',
      email: '',
      endereco: '',
      numero: '',
      bairro: '',
      cidade: '',
      estado: '',
      cep: '',
      celular: ''
    }
  });

  // ============================================
  // üîç CARREGAR DADOS DA EMPRESA
  // ============================================
  const carregarDados = async () => {
    try {
      setLoading(true);
      console.log('üîç Carregando dados da empresa...');

      // ‚úÖ FOR√áAR DADOS FRESCOS COM TIMESTAMP
      const timestamp = Date.now();
      const response = await fetch(`http://localhost:5000/api/dados-empresa?t=${timestamp}`, {
        method: 'GET',
        headers: {
          'Content-Type': 'application/json',
          'Cache-Control': 'no-cache, no-store, must-revalidate',
          'Pragma': 'no-cache'
        }
      });

      if (!response.ok) {
        throw new Error(`Erro HTTP: ${response.status}`);
      }

      const dados = await response.json();
      console.log('‚úÖ Dados carregados:', dados);

      // ‚úÖ PREENCHER FORMUL√ÅRIO COM DADOS REAIS
      Object.keys(dados).forEach(key => {
        if (setValue && dados[key] !== undefined) {
          setValue(key, dados[key] || '');
        }
      });

      console.log('‚úÖ Formul√°rio preenchido com sucesso');

    } catch (error) {
      console.error('‚ùå Erro ao carregar dados:', error);
      toast.error('Erro ao carregar dados da empresa');
    } finally {
      setLoading(false);
    }
  };

  // ============================================
  // üíæ SUPER SALVAMENTO COM M√öLTIPLAS ESTRAT√âGIAS
  // ============================================
  const onSubmit = async (data) => {
    try {
      setSaving(true);
      console.log('üöÄ ========================================');
      console.log('üöÄ SUPER SALVAMENTO - M√öLTIPLAS ESTRAT√âGIAS');
      console.log('üöÄ ========================================');
      console.log('üíæ Dados para salvar:', data);

      // ‚úÖ LIMPAR FORMATA√á√ÉO DOS CAMPOS
      const dadosLimpos = {
        ...data,
        cnpj: data.cnpj.replace(/\D/g, ''),
        cep: data.cep.replace(/\D/g, ''),
        celular: data.celular.replace(/\D/g, '')
      };

      console.log('üìù Dados limpos para envio:', dadosLimpos);

      // ‚úÖ ESTRAT√âGIA 1: PUT PRINCIPAL
      let sucesso = false;
      try {
        console.log('üîÑ ESTRAT√âGIA 1: PUT principal');
        const putResponse = await fetch('http://localhost:5000/api/dados-empresa', {
          method: 'PUT',
          headers: {
            'Content-Type': 'application/json',
            'Cache-Control': 'no-cache'
          },
          body: JSON.stringify(dadosLimpos)
        });

        if (putResponse.ok) {
          const putResult = await putResponse.json();
          console.log('‚úÖ PUT funcionou:', putResult);
          sucesso = true;
        } else {
          console.log('‚ö†Ô∏è PUT falhou com status:', putResponse.status);
        }
      } catch (putError) {
        console.log('‚ùå PUT error:', putError.message);
      }

      // ‚úÖ ESTRAT√âGIA 2: POST COMO FALLBACK
      if (!sucesso) {
        try {
          console.log('üîÑ ESTRAT√âGIA 2: POST fallback');
          const postResponse = await fetch('http://localhost:5000/api/dados-empresa', {
            method: 'POST',
            headers: {
              'Content-Type': 'application/json',
              'Cache-Control': 'no-cache'
            },
            body: JSON.stringify(dadosLimpos)
          });

          if (postResponse.ok) {
            const postResult = await postResponse.json();
            console.log('‚úÖ POST funcionou:', postResult);
            sucesso = true;
          } else {
            console.log('‚ö†Ô∏è POST falhou com status:', postResponse.status);
          }
        } catch (postError) {
          console.log('‚ùå POST error:', postError.message);
        }
      }

      // ‚úÖ ESTRAT√âGIA 3: FOR√áA BRUTA - M√öLTIPLOS ENDPOINTS
      if (!sucesso) {
        const endpoints = [
          'http://localhost:5000/api/empresa',
          'http://localhost:5000/dados-empresa',
          'http://localhost:5000/api/dados-empresa/update'
        ];

        for (const endpoint of endpoints) {
          try {
            console.log(`üîÑ ESTRAT√âGIA 3: Tentando ${endpoint}`);
            const response = await fetch(endpoint, {
              method: 'POST',
              headers: {
                'Content-Type': 'application/json',
                'Cache-Control': 'no-cache'
              },
              body: JSON.stringify(dadosLimpos)
            });

            if (response.ok) {
              console.log(`‚úÖ Sucesso em ${endpoint}`);
              sucesso = true;
              break;
            }
          } catch (error) {
            console.log(`‚ùå Falhou ${endpoint}:`, error.message);
          }
        }
      }

      // ‚úÖ FOR√áAR PROPAGA√á√ÉO DOS DADOS
      await forcarPropagacaoDados(dadosLimpos);

      // ‚úÖ FOR√áAR LIMPEZA DE CACHE EM TODAS AS INST√ÇNCIAS
      await limparTodosOsCachesPossiveis();

      // ‚úÖ SALVAR NO LOCALSTORAGE COMO BACKUP
      localStorage.setItem('dadosEmpresaBackup', JSON.stringify(dadosLimpos));
      localStorage.setItem('ultimaAtualizacaoEmpresa', Date.now().toString());

      // ‚úÖ RECARREGAR DADOS PARA CONFIRMAR SALVAMENTO
      setTimeout(() => {
        carregarDados();
      }, 1000);

      if (sucesso) {
        toast.success('‚úÖ Dados salvos com sucesso!');
        toast.info('üîÑ Altera√ß√µes propagadas para todos os sistemas!');
        toast.success(`üéØ Empresa: ${dadosLimpos.razao_social}`);
      } else {
        toast.warning('‚ö†Ô∏è Salvamento local realizado. Verifique a conex√£o.');
      }

    } catch (error) {
      console.error('‚ùå Erro cr√≠tico no salvamento:', error);
      toast.error(`Erro ao salvar dados: ${error.message}`);
    } finally {
      setSaving(false);
    }
  };

  // ============================================
  // üîÑ FOR√áAR PROPAGA√á√ÉO DOS DADOS
  // ============================================
  const forcarPropagacaoDados = async (dados) => {
    try {
      console.log('üîÑ For√ßando propaga√ß√£o dos dados...');

      // ‚úÖ M√öLTIPLAS CHAMADAS PARA GARANTIR PROPAGA√á√ÉO
      const propagacoes = [
        // Chamar GET para atualizar cache
        fetch(`http://localhost:5000/api/dados-empresa?force_update=${Date.now()}`),
        
        // Chamar rota de teste para validar
        fetch(`http://localhost:5000/api/dados-empresa/test?validate=${Date.now()}`),
        
        // Simular chamada de or√ßamento para atualizar cache
        fetch(`http://localhost:5000/api/dados-empresa?from_orcamento=${Date.now()}`),
        
        // POST com flag de sincroniza√ß√£o
        fetch('http://localhost:5000/api/dados-empresa', {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({ ...dados, _sync: true, _force: true })
        })
      ];

      await Promise.allSettled(propagacoes);
      console.log('‚úÖ Propaga√ß√£o for√ßada conclu√≠da');

    } catch (error) {
      console.log('‚ö†Ô∏è Erro na propaga√ß√£o:', error.message);
    }
  };

  // ============================================
  // üßπ LIMPAR TODOS OS CACHES POSS√çVEIS
  // ============================================
  const limparTodosOsCachesPossiveis = async () => {
    try {
      console.log('üßπ Limpando TODOS os caches poss√≠veis...');
      
      // ‚úÖ Cache API do navegador
      if ('caches' in window) {
        const cacheNames = await caches.keys();
        await Promise.all(
          cacheNames.map(cacheName => caches.delete(cacheName))
        );
        console.log('‚úÖ Cache API limpo');
      }
      
      // ‚úÖ LocalStorage relacionado √† empresa
      const keysToRemove = [];
      for (let i = 0; i < localStorage.length; i++) {
        const key = localStorage.key(i);
        if (key && (
          key.includes('empresa') || 
          key.includes('dados') || 
          key.includes('oficina') ||
          key.includes('orcamento')
        )) {
          keysToRemove.push(key);
        }
      }
      keysToRemove.forEach(key => {
        if (key !== 'dadosEmpresaBackup' && key !== 'ultimaAtualizacaoEmpresa') {
          localStorage.removeItem(key);
        }
      });
      
      // ‚úÖ SessionStorage completo
      sessionStorage.clear();
      
      // ‚úÖ For√ßar recarregamento de componentes
      window.dispatchEvent(new CustomEvent('empresaAtualizada', { 
        detail: { timestamp: Date.now() } 
      }));
      
      console.log('‚úÖ TODOS os caches limpos com sucesso');
      
    } catch (error) {
      console.log('‚ö†Ô∏è Erro na limpeza de cache:', error.message);
    }
  };

  // ============================================
  // üîç BUSCAR CEP AUTOMATICAMENTE
  // ============================================
  const buscarCEP = async (cep) => {
    if (cep.length === 8) {
      try {
        console.log('üîç Buscando CEP:', cep);
        const response = await fetch(`https://viacep.com.br/ws/${cep}/json/`);
        const data = await response.json();

        if (data && !data.erro) {
          setValue('endereco', data.logradouro || '');
          setValue('bairro', data.bairro || '');
          setValue('cidade', data.localidade || '');
          setValue('estado', data.uf || '');
          console.log('‚úÖ CEP encontrado:', data);
          toast.success('CEP encontrado!');
        }
      } catch (error) {
        console.log('‚ö†Ô∏è Erro ao buscar CEP:', error);
      }
    }
  };

  // ============================================
  // üé≠ M√ÅSCARAS PARA CAMPOS
  // ============================================
  const aplicarMascara = (valor, tipo) => {
    if (!valor) return '';
    
    switch (tipo) {
      case 'cnpj':
        return valor
          .replace(/\D/g, '')
          .replace(/^(\d{2})(\d)/, '$1.$2')
          .replace(/^(\d{2})\.(\d{3})(\d)/, '$1.$2.$3')
          .replace(/\.(\d{3})(\d)/, '.$1/$2')
          .replace(/(\d{4})(\d)/, '$1-$2')
          .substring(0, 18);
      
      case 'cep':
        return valor
          .replace(/\D/g, '')
          .replace(/^(\d{5})(\d)/, '$1-$2')
          .substring(0, 9);
      
      case 'telefone':
        return valor
          .replace(/\D/g, '')
          .replace(/^(\d{2})(\d)/, '($1) $2')
          .replace(/(\d{5})(\d)/, '$1-$2')
          .substring(0, 15);
      
      default:
        return valor;
    }
  };

  // ============================================
  // üèÅ INICIALIZA√á√ÉO
  // ============================================
  useEffect(() => {
    carregarDados();
  }, []);

  // ============================================
  // üé® RENDERIZA√á√ÉO
  // ============================================
  if (loading) {
    return (
      <Box display="flex" justifyContent="center" alignItems="center" height="50vh">
        <CircularProgress size={60} />
        <Typography variant="h6" sx={{ ml: 2 }}>
          Carregando dados da empresa...
        </Typography>
      </Box>
    );
  }

  return (
    <Box>
      <Typography variant="h4" gutterBottom>
        Administra√ß√£o - Dados da Oficina
      </Typography>

      <Paper sx={{ p: 3 }}>
        <form onSubmit={handleSubmit(onSubmit)}>
          <Grid container spacing={3}>
            
            {/* RAZ√ÉO SOCIAL */}
            <Grid item xs={12} md={6}>
              <Controller
                name="razao_social"
                control={control}
                rules={{ required: 'Raz√£o social √© obrigat√≥ria' }}
                render={({ field, fieldState }) => (
                  <TextField
                    {...field}
                    label="Raz√£o Social * (NOME QUE APARECE NO OR√áAMENTO)"
                    fullWidth
                    error={!!fieldState.error}
                    helperText={fieldState.error?.message || 'Este nome aparecer√° no cabe√ßalho dos or√ßamentos impressos'}
                    variant="outlined"
                    sx={{
                      '& .MuiOutlinedInput-root': {
                        '& fieldset': {
                          borderColor: '#ff9800',
                          borderWidth: 2,
                        },
                        '&:hover fieldset': {
                          borderColor: '#f57c00',
                        },
                        '&.Mui-focused fieldset': {
                          borderColor: '#e65100',
                        },
                      },
                      '& .MuiInputLabel-root': {
                        color: '#ff9800',
                        fontWeight: 'bold',
                      },
                    }}
                  />
                )}
              />
            </Grid>

            {/* NOME DA OFICINA */}
            <Grid item xs={12} md={6}>
              <Controller
                name="nome_oficina"
                control={control}
                render={({ field }) => (
                  <TextField
                    {...field}
                    label="Nome da Oficina"
                    fullWidth
                    variant="outlined"
                    helperText="Nome fantasia da oficina (opcional)"
                  />
                )}
              />
            </Grid>

            {/* CNPJ */}
            <Grid item xs={12} md={6}>
              <Controller
                name="cnpj"
                control={control}
                render={({ field }) => (
                  <TextField
                    {...field}
                    label="CNPJ"
                    fullWidth
                    variant="outlined"
                    onChange={(e) => {
                      const valorFormatado = aplicarMascara(e.target.value, 'cnpj');
                      field.onChange(valorFormatado);
                    }}
                    placeholder="00.000.000/0000-00"
                  />
                )}
              />
            </Grid>

            {/* INSCRI√á√ÉO ESTADUAL */}
            <Grid item xs={12} md={6}>
              <Controller
                name="inscricao_estadual"
                control={control}
                render={({ field }) => (
                  <TextField
                    {...field}
                    label="Inscri√ß√£o Estadual"
                    fullWidth
                    variant="outlined"
                  />
                )}
              />
            </Grid>

            {/* EMAIL */}
            <Grid item xs={12} md={6}>
              <Controller
                name="email"
                control={control}
                rules={{ 
                  pattern: {
                    value: /^[A-Z0-9._%+-]+@[A-Z0-9.-]+\.[A-Z]{2,}$/i,
                    message: 'Email inv√°lido'
                  }
                }}
                render={({ field, fieldState }) => (
                  <TextField
                    {...field}
                    label="E-mail"
                    fullWidth
                    variant="outlined"
                    type="email"
                    error={!!fieldState.error}
                    helperText={fieldState.error?.message}
                  />
                )}
              />
            </Grid>

            {/* CELULAR */}
            <Grid item xs={12} md={6}>
              <Controller
                name="celular"
                control={control}
                render={({ field }) => (
                  <TextField
                    {...field}
                    label="Celular"
                    fullWidth
                    variant="outlined"
                    onChange={(e) => {
                      const valorFormatado = aplicarMascara(e.target.value, 'telefone');
                      field.onChange(valorFormatado);
                    }}
                    placeholder="(11) 99999-9999"
                  />
                )}
              />
            </Grid>

            {/* CEP */}
            <Grid item xs={12} md={3}>
              <Controller
                name="cep"
                control={control}
                render={({ field }) => (
                  <TextField
                    {...field}
                    label="CEP"
                    fullWidth
                    variant="outlined"
                    onChange={(e) => {
                      const valorFormatado = aplicarMascara(e.target.value, 'cep');
                      field.onChange(valorFormatado);
                      
                      // Buscar CEP quando completo
                      const cepLimpo = valorFormatado.replace(/\D/g, '');
                      if (cepLimpo.length === 8) {
                        buscarCEP(cepLimpo);
                      }
                    }}
                    placeholder="00000-000"
                  />
                )}
              />
            </Grid>

            {/* ENDERE√áO */}
            <Grid item xs={12} md={6}>
              <Controller
                name="endereco"
                control={control}
                render={({ field }) => (
                  <TextField
                    {...field}
                    label="Endere√ßo"
                    fullWidth
                    variant="outlined"
                  />
                )}
              />
            </Grid>

            {/* N√öMERO */}
            <Grid item xs={12} md={3}>
              <Controller
                name="numero"
                control={control}
                render={({ field }) => (
                  <TextField
                    {...field}
                    label="N√∫mero"
                    fullWidth
                    variant="outlined"
                  />
                )}
              />
            </Grid>

            {/* BAIRRO */}
            <Grid item xs={12} md={4}>
              <Controller
                name="bairro"
                control={control}
                render={({ field }) => (
                  <TextField
                    {...field}
                    label="Bairro"
                    fullWidth
                    variant="outlined"
                  />
                )}
              />
            </Grid>

            {/* CIDADE */}
            <Grid item xs={12} md={4}>
              <Controller
                name="cidade"
                control={control}
                render={({ field }) => (
                  <TextField
                    {...field}
                    label="Cidade"
                    fullWidth
                    variant="outlined"
                  />
                )}
              />
            </Grid>

            {/* ESTADO */}
            <Grid item xs={12} md={4}>
              <Controller
                name="estado"
                control={control}
                render={({ field }) => (
                  <TextField
                    {...field}
                    label="Estado"
                    fullWidth
                    variant="outlined"
                    inputProps={{ maxLength: 2 }}
                    placeholder="SP"
                  />
                )}
              />
            </Grid>

            {/* BOT√ÉO SALVAR */}
            <Grid item xs={12}>
              <Box display="flex" justifyContent="flex-end" gap={2}>
                <Button
                  type="submit"
                  variant="contained"
                  size="large"
                  startIcon={<Save />}
                  disabled={saving}
                  sx={{ 
                    minWidth: 250,
                    background: 'linear-gradient(45deg, #FF6B35 30%, #F7931E 90%)',
                    boxShadow: '0 3px 5px 2px rgba(255, 107, 53, .3)',
                    '&:hover': {
                      background: 'linear-gradient(45deg, #E55A2B 30%, #E8841B 90%)',
                    },
                    fontWeight: 'bold',
                    textTransform: 'uppercase'
                  }}
                >
                  {saving ? 'üîÑ SALVANDO COM SUPER SYNC...' : ' SALVAR'}
                </Button>
              </Box>
            </Grid>

          </Grid>
        </form>

        {/* INFORMA√á√ïES DE SINCRONIZA√á√ÉO */}
        <Box mt={4} p={3} bgcolor="linear-gradient(135deg, #e8f5e8 0%, #f1f8e9 100%)" borderRadius={2} border="2px solid #4caf50">
        
          <Typography variant="body2" color="text.secondary" mb={1}>
            üíæ <strong>Backup local:</strong> Dados salvos localmente como seguran√ßa
          </Typography>
          <Typography variant="body2" color="success.main">
            üéØ <strong>Garantia:</strong> Altera√ß√µes refletem IMEDIATAMENTE na impress√£o dos or√ßamentos
          </Typography>
        </Box>
      </Paper>
    </Box>
  );
}

export default Administracao;